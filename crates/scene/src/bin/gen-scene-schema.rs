//! `gen-scene-schema` — emit a `scene.kdl-schema` file by walking facet SHAPE
//! for every AST type in `ark_scene::ast`.
//!
//! Phase A (this binary): structural schema — node names, argument count +
//! types, property names + types, required/optional children. Drives editor
//! completion. Ships at `crates/scene/share/scene.kdl-schema` for editor
//! consumption per T-1.5 / cavekit-scene.md R1, R13.
//!
//! Phase B (follow-up, tracked separately): value-constraint attributes —
//! `#[facet(kdl_pattern = "…", kdl_enum = [...])]` on fields, surfaced as
//! `pattern` / `enum` child nodes under `prop` / `value` in the schema.
//! Will drive editor diagnostics once the custom attributes exist.
//!
//! The schema emitter is PURE REFLECTION: every node name, every argument,
//! every property, every child relationship is derived by walking
//! `Shape.ty`, `Shape.def`, `Field` attributes, and `type_identifier`. There
//! is no hand-maintained table. When the AST evolves, the schema updates
//! automatically on the next run.
//!
//! Run:
//! ```sh
//! cargo run -p ark-scene --bin gen-scene-schema
//! ```
//!
//! The generated file is checked in under `crates/scene/share/`. CI runs
//! this binary and diffs the output against the committed file to catch
//! drift — if the AST changes and nobody regenerated the schema, CI fails.
//!
//! # Facet SHAPE gotchas (remember for future reflection tasks)
//!
//! * `Shape.type_identifier` is the name WITHOUT generics (`"Vec"`,
//!   `"Option"`, `"SceneNode"`). For the fully-qualified name with generics,
//!   use `Shape.vtable.type_name` — but the identifier alone is enough to
//!   spot user types vs container wrappers.
//! * `Def::Option(o)` exposes inner shape via `o.t` (a `&'static Shape`).
//!   `Def::List(l)` likewise via `l.t()`. Walking containers is the
//!   mechanical way to unwrap `Option<Vec<T>>` etc.
//! * `Field.is_element()` / `is_elements()` / `is_argument()` /
//!   `is_arguments_plural()` / `is_property()` — facet-core exposes these
//!   as convenience helpers that check the `kdl::*` namespaced attributes.
//!   Prefer them over scanning `Field.attributes` by hand.
//! * A KDL `#[facet(kdl::child)]` field on an `Option<T>` means an optional
//!   child; on `Vec<T>` it becomes `#[facet(kdl::children)]` (plural) and
//!   means zero-or-more. The derive macro enforces this shape.
//! * `Field.rename` holds the `#[facet(rename = "foo")]` override — the
//!   effective KDL node/property name is `rename.unwrap_or(name)`, exposed
//!   as `Field::effective_name()`.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::path::PathBuf;

use facet::{Def, Facet, Shape, Type, UserType};

use ark_scene::ast::SceneDoc;

/// Result of walking the AST's SHAPE graph — one entry per user-defined
/// struct reachable from `SceneDoc::SHAPE`, keyed by `type_identifier`.
///
/// Ordered (BTreeMap) so output is deterministic across runs, which is
/// what the CI drift check relies on.
type TypeIndex = BTreeMap<&'static str, &'static Shape>;

/// Entry point.
///
/// Resolves the workspace's `crates/scene/share/` directory relative to
/// this binary's `CARGO_MANIFEST_DIR` (set by cargo at build time), creates
/// it if missing, walks SHAPE, writes `scene.kdl-schema`.
fn main() -> std::io::Result<()> {
    let output_path = share_dir().join("scene.kdl-schema");
    std::fs::create_dir_all(output_path.parent().unwrap())?;

    let schema = generate_schema();
    std::fs::write(&output_path, schema.as_bytes())?;

    eprintln!("wrote {} bytes to {}", schema.len(), output_path.display());
    Ok(())
}

/// Workspace-relative `crates/scene/share/` path. `CARGO_MANIFEST_DIR` at
/// compile time is the crate root (`crates/scene/`), so `share/` is one
/// hop below it. This works whether the binary is invoked via
/// `cargo run -p ark-scene --bin gen-scene-schema` or from a hand-launched
/// target-dir build.
fn share_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("share")
}

// ---------------------------------------------------------------------------
// Schema generation
// ---------------------------------------------------------------------------

/// Produce the full `scene.kdl-schema` document as a string.
fn generate_schema() -> String {
    let mut types: TypeIndex = BTreeMap::new();
    collect_user_types(SceneDoc::SHAPE, &mut types);

    let mut out = String::new();
    writeln!(
        &mut out,
        "// Generated by `cargo run -p ark-scene --bin gen-scene-schema`."
    )
    .unwrap();
    writeln!(
        &mut out,
        "// DO NOT EDIT BY HAND. Source of truth: crates/scene/src/ast.rs"
    )
    .unwrap();
    writeln!(&mut out, "// (walked via facet SHAPE reflection).").unwrap();
    writeln!(&mut out).unwrap();
    writeln!(&mut out, "document {{").unwrap();
    writeln!(&mut out, "    info {{").unwrap();
    writeln!(&mut out, "        title \"ark scene file\"").unwrap();
    writeln!(
        &mut out,
        "        description \"Reactive KDL config for ark (cavekit-scene.md R1).\""
    )
    .unwrap();
    writeln!(&mut out, "        version \"0.1.0-phase-a\"").unwrap();
    writeln!(&mut out, "    }}").unwrap();
    writeln!(&mut out).unwrap();

    // The KDL document root. `SceneDoc` has a single `kdl::child` field
    // pointing at `SceneNode`, so the schema declares exactly one top-level
    // node name: `scene`.
    if let Some(doc_field) = single_root_child(SceneDoc::SHAPE) {
        let root_name = kdl_node_name_for_field(doc_field);
        let root_shape = doc_field.shape().inner_user_shape();
        if let Some(root_shape) = root_shape {
            emit_node(&mut out, &root_name, root_shape, 1);
        }
    }

    // Every other user type is emitted under `definitions` for reference
    // via `ref`. For Phase A we keep the tree inline (children repeated
    // where they appear) — `definitions` are informational only here, but
    // they give editors a flat type index to hover-resolve against.
    writeln!(&mut out).unwrap();
    writeln!(&mut out, "    definitions {{").unwrap();
    for (name, shape) in &types {
        // Skip the root — already emitted.
        if *name == "SceneDoc" || *name == "SceneNode" {
            continue;
        }
        emit_definition(&mut out, name, shape, 2);
    }
    writeln!(&mut out, "    }}").unwrap();
    writeln!(&mut out, "}}").unwrap();
    out
}

/// Walk the SHAPE graph starting at `shape`, inserting every reachable
/// user struct into `acc`. Non-user shapes (scalars, Vec, Option, …) are
/// traversed through but not recorded — the schema describes user types.
fn collect_user_types(shape: &'static Shape, acc: &mut TypeIndex) {
    let user = match shape.inner_user_shape() {
        Some(u) => u,
        None => return,
    };
    if acc.contains_key(user.type_identifier) {
        return;
    }
    acc.insert(user.type_identifier, user);

    if let Type::User(UserType::Struct(st)) = &user.ty {
        for field in st.fields {
            collect_user_types(field.shape(), acc);
        }
    }
}

/// For `SceneDoc { scene: SceneNode }`, return the single `scene` field.
/// The wrapper struct must have exactly one field for this to make sense.
fn single_root_child(shape: &'static Shape) -> Option<&'static facet::Field> {
    if let Type::User(UserType::Struct(st)) = &shape.ty
        && st.fields.len() == 1
    {
        return Some(&st.fields[0]);
    }
    None
}

// ---------------------------------------------------------------------------
// Node emission
// ---------------------------------------------------------------------------

/// Emit a `node "<name>" { … }` block describing a single AST struct.
///
/// Layout:
/// ```kdl
/// node "scene" {
///     description "…"            // type doc-comment
///     value "name" {               // kdl::argument fields, one per
///         type "string"
///         min 1; max 1
///     }
///     prop "max-cascade-depth" {   // kdl::property fields
///         type "u32"
///         required #false
///     }
///     children {
///         node "extends" { min 0; max 1; ref "ExtendsNode" }
///         node "include" { min 0; ref "IncludeNode" }
///         …
///     }
/// }
/// ```
fn emit_node(out: &mut String, name: &str, shape: &'static Shape, depth: usize) {
    let indent = indent(depth);
    writeln!(out, "{indent}node \"{name}\" {{").unwrap();

    // Type doc-comment (the `///` block on the struct itself).
    write_doc(out, shape.doc, depth + 1);

    let fields = match &shape.ty {
        Type::User(UserType::Struct(st)) => st.fields,
        _ => &[],
    };

    // Arguments (positional values).
    for field in fields {
        if field.is_argument() {
            emit_value(out, field, depth + 1);
        }
    }
    // Properties (named key=value).
    for field in fields {
        if field.is_property() {
            emit_prop(out, field, depth + 1);
        }
    }
    // Children.
    let child_fields: Vec<_> = fields
        .iter()
        .filter(|f| f.is_element() || f.is_elements())
        .collect();
    if !child_fields.is_empty() {
        writeln!(out, "{}children {{", indent_for(depth + 1)).unwrap();
        for field in &child_fields {
            emit_child(out, field, depth + 2);
        }
        writeln!(out, "{}}}", indent_for(depth + 1)).unwrap();
    }

    writeln!(out, "{indent}}}").unwrap();
}

/// Emit a `value "<field>" { type "…"; min/max … }` entry describing a
/// `#[facet(kdl::argument)]` or `#[facet(kdl::arguments)]` field.
fn emit_value(out: &mut String, field: &facet::Field, depth: usize) {
    let name = field.effective_name();
    let indent = indent(depth);
    let (inner, optional) = unwrap_container(field.shape());
    let type_name = scalar_type_name(inner);
    let plural = field.is_arguments_plural();

    writeln!(out, "{indent}value \"{name}\" {{").unwrap();
    write_doc(out, field.doc, depth + 1);
    writeln!(out, "{}type \"{type_name}\"", indent_for(depth + 1)).unwrap();
    if plural {
        writeln!(out, "{}min 0", indent_for(depth + 1)).unwrap();
    } else if optional {
        writeln!(out, "{}min 0; max 1", indent_for(depth + 1)).unwrap();
    } else {
        writeln!(out, "{}min 1; max 1", indent_for(depth + 1)).unwrap();
    }
    writeln!(out, "{indent}}}").unwrap();
}

/// Emit a `prop "<field>" { type "…"; required #… }` entry describing a
/// `#[facet(kdl::property)]` field.
fn emit_prop(out: &mut String, field: &facet::Field, depth: usize) {
    let name = field.effective_name();
    let indent = indent(depth);
    let (inner, optional) = unwrap_container(field.shape());
    let type_name = scalar_type_name(inner);
    // Effective required = not Option and no #[facet(default)].
    let required = !optional && !field.has_default();

    writeln!(out, "{indent}prop \"{name}\" {{").unwrap();
    write_doc(out, field.doc, depth + 1);
    writeln!(out, "{}type \"{type_name}\"", indent_for(depth + 1)).unwrap();
    writeln!(
        out,
        "{}required {}",
        indent_for(depth + 1),
        if required { "#true" } else { "#false" }
    )
    .unwrap();
    writeln!(out, "{indent}}}").unwrap();
}

/// Emit a child-node entry in a parent's `children { … }` block. The entry
/// references (by `ref`) the full `node` definition in `definitions { … }`.
fn emit_child(out: &mut String, field: &facet::Field, depth: usize) {
    let name = kdl_node_name_for_field(field);
    let indent = indent(depth);
    let plural = field.is_elements();
    let (inner, optional) = unwrap_container(field.shape());
    let ref_id = inner.type_identifier;

    writeln!(out, "{indent}node \"{name}\" {{").unwrap();
    write_doc(out, field.doc, depth + 1);
    if plural {
        writeln!(out, "{}min 0", indent_for(depth + 1)).unwrap();
    } else if optional {
        writeln!(out, "{}min 0; max 1", indent_for(depth + 1)).unwrap();
    } else {
        writeln!(out, "{}min 1; max 1", indent_for(depth + 1)).unwrap();
    }
    writeln!(out, "{}ref \"{ref_id}\"", indent_for(depth + 1)).unwrap();
    writeln!(out, "{indent}}}").unwrap();
}

/// Emit a type definition in the `definitions { … }` block.
fn emit_definition(out: &mut String, id: &str, shape: &'static Shape, depth: usize) {
    let indent = indent(depth);
    writeln!(out, "{indent}node id=\"{id}\" {{").unwrap();
    write_doc(out, shape.doc, depth + 1);

    let fields = match &shape.ty {
        Type::User(UserType::Struct(st)) => st.fields,
        _ => &[],
    };
    for field in fields {
        if field.is_argument() {
            emit_value(out, field, depth + 1);
        }
    }
    for field in fields {
        if field.is_property() {
            emit_prop(out, field, depth + 1);
        }
    }
    let child_fields: Vec<_> = fields
        .iter()
        .filter(|f| f.is_element() || f.is_elements())
        .collect();
    if !child_fields.is_empty() {
        writeln!(out, "{}children {{", indent_for(depth + 1)).unwrap();
        for field in &child_fields {
            emit_child(out, field, depth + 2);
        }
        writeln!(out, "{}}}", indent_for(depth + 1)).unwrap();
    }
    writeln!(out, "{indent}}}").unwrap();
}

