#[cfg(test)]
mod tests {
    use super::*;

    /// Smoke test: the generated schema mentions the root `scene` node,
    /// every major top-level child node, and the `SceneNode` identifier.
    /// This is the guardrail against silent SHAPE walks that miss branches.
    #[test]
    fn schema_mentions_top_level_node_set() {
        let schema = generate_schema();
        // Root node name.
        assert!(schema.contains("node \"scene\""), "missing root scene node");
        // Properties on SceneNode surface as prop entries.
        assert!(
            schema.contains("prop \"max-cascade-depth\""),
            "missing max-cascade-depth prop"
        );
        // R1 child node set — every top-level child must appear in the
        // children block under the root.
        for expected in [
            "\"extends\"",
            "\"include\"",
            "\"use\"",
            "\"layout\"",
            "\"plugin\"",
            "\"on\"",
            "\"keybind\"",
            "\"engine\"",
            "\"clear-reactions\"",
            "\"clear-keybinds\"",
            "\"disable-plugins\"",
        ] {
            assert!(
                schema.contains(expected),
                "schema missing top-level child {expected}; got:\n{schema}"
            );
        }
        // Definitions block should list downstream struct ids.
        assert!(
            schema.contains("id=\"LayoutNode\""),
            "missing LayoutNode definition"
        );
        assert!(
            schema.contains("id=\"PluginNode\""),
            "missing PluginNode definition"
        );
    }

    /// Unwrapping should peel `Option<Vec<T>>` down to `T`.
    #[test]
    fn unwrap_container_handles_option_of_vec() {
        use ark_scene::ast::SceneNode;
        // `SceneNode::ons` is `Vec<OnNode>` (plural, no Option).
        let fields = match &SceneNode::SHAPE.ty {
            Type::User(UserType::Struct(st)) => st.fields,
            _ => panic!("SceneNode should be a struct"),
        };
        let ons = fields.iter().find(|f| f.name == "ons").expect("ons field");
        let (inner, optional) = unwrap_container(ons.shape());
        assert_eq!(inner.type_identifier, "OnNode");
        assert!(!optional, "Vec<OnNode> is not optional");

        // `SceneNode::layout` is `Option<LayoutNode>`.
        let layout = fields
            .iter()
            .find(|f| f.name == "layout")
            .expect("layout field");
        let (inner, optional) = unwrap_container(layout.shape());
        assert_eq!(inner.type_identifier, "LayoutNode");
        assert!(optional, "Option<LayoutNode> is optional");
    }
}
