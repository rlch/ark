/// Pick the KDL node name a child field should advertise.
///
/// facet-kdl's matching rules (see `facet-format::deserializer`):
/// * For a `#[facet(kdl::child)]` single-child field: the KDL node name is
///   either the field's `rename` attribute (if set) or the field name
///   verbatim.
/// * For a `#[facet(kdl::children)]` repeated field WITHOUT `rename`:
///   the KDL node name is the SINGULAR form of the field name (via
///   `facet_singularize`, which handles irregular English plurals).
///   Example: `plugins: Vec<PluginNode>` matches KDL nodes named `plugin`.
/// * For a `#[facet(kdl::children)]` repeated field WITH `rename`: we
///   emit the rename verbatim. The rename is the author's explicit node
///   name choice — R1 lists several plural KDL node names
///   (`clear-reactions`) where the intent is clearly to keep the plural
///   KDL name, so re-singularizing would break parse. See
///   `SceneNode::clear_reactions` — `rename = "clear-reactions"` is the
///   intended node name.
///
/// We replicate those rules here so the schema announces the same names
/// the parser accepts.
fn kdl_node_name_for_field(field: &facet::Field) -> String {
    if let Some(renamed) = field.rename {
        return renamed.to_string();
    }
    if field.is_elements() {
        singularize(field.name)
    } else {
        field.name.to_string()
    }
}

/// Lightweight English-plural → singular. Matches the common-case subset
/// of `facet_singularize::singularize` sufficient for the node names in
/// our AST (`plugins` -> `plugin`, `includes` -> `include`,
/// `keybinds` -> `keybind`, `ons` -> `on`, `uses` -> `use`,
/// `subscribes` -> `subscribe`, `panes` -> `pane`, `tabs` -> `tab`).
/// Rules: drop a trailing `ies` → `y`, trailing `es` on known `-es`
/// suffixes → strip `es`, else drop trailing `s`. Non-plural inputs pass
/// through unchanged.
///
/// Edge case: fields renamed to already-plural KDL names (e.g.
/// `clear_reactions` → `rename = "clear-reactions"`) get `clear-reaction`.
/// This matches facet-kdl's behaviour — the rename target is still
/// singularized when it's a `kdl::children` field — so the schema is
/// consistent with parse.
fn singularize(s: &str) -> String {
    // Irregular / rule-based English singularization limited to patterns we
    // actually hit. For anything more exotic, use `#[facet(rename)]`.
    if let Some(stripped) = s.strip_suffix("ies") {
        return format!("{stripped}y");
    }
    for esuffix in ["ches", "shes", "sses", "xes", "zes"] {
        if let Some(stripped) = s.strip_suffix(esuffix) {
            return format!("{stripped}{}", &esuffix[..esuffix.len() - 2]);
        }
    }
    if let Some(stripped) = s.strip_suffix('s') {
        return stripped.to_string();
    }
    s.to_string()
}

/// Walk `Option<…>` / `Vec<…>` wrappers down to the first user shape.
/// Returns `(inner_shape, is_optional)`. `is_optional` is true when the
/// outermost wrapper was `Option`.
fn unwrap_container(shape: &'static Shape) -> (&'static Shape, bool) {
    match &shape.def {
        Def::Option(o) => {
            let (inner, _) = unwrap_container(o.t);
            (inner, true)
        }
        Def::List(l) => {
            let (inner, _) = unwrap_container(l.t());
            (inner, false)
        }
        _ => (shape, false),
    }
}

/// Map a shape onto a kdl-schema type tag (`string`, `u32`, `bool`, …).
/// User structs are reported by identifier so editors can cross-link into
/// the `definitions` block.
fn scalar_type_name(shape: &'static Shape) -> &'static str {
    // Facet's `type_identifier` is the name without generics, which is
    // exactly what we want for primitives. For user types we still return
    // the identifier (callers doing property/value typing rarely reference
    // a user type there, but it's informational).
    match shape.type_identifier {
        "String" | "str" => "string",
        "bool" => "bool",
        other => other,
    }
}

/// Write a type's or field's rustdoc lines as a schema `description`
/// string. Facet collects each `///` line as an entry in `doc`; we join
/// with spaces so the schema stays single-line per description. Each line
/// is already space-prefixed by the facet macros, so we trim before join.
fn write_doc(out: &mut String, doc: &'static [&'static str], depth: usize) {
    if doc.is_empty() {
        return;
    }
    let joined = doc
        .iter()
        .map(|line| line.trim())
        .collect::<Vec<_>>()
        .join(" ");
    // Escape embedded quotes for the KDL string literal.
    let escaped = joined.replace('\\', "\\\\").replace('"', "\\\"");
    writeln!(out, "{}description \"{}\"", indent_for(depth), escaped).unwrap();
}

/// Extension trait: follow transparent wrappers and container wrappers
/// down to the first user-type shape. Returns `None` if none exists
/// (e.g. pure scalars).
trait InnerUserShape {
    fn inner_user_shape(&'static self) -> Option<&'static Shape>;
}

impl InnerUserShape for Shape {
    fn inner_user_shape(&'static self) -> Option<&'static Shape> {
        match &self.ty {
            Type::User(UserType::Struct(_))
            | Type::User(UserType::Enum(_))
            | Type::User(UserType::Union(_)) => Some(self),
            _ => match &self.def {
                Def::Option(o) => o.t.inner_user_shape(),
                Def::List(l) => l.t().inner_user_shape(),
                _ => None,
            },
        }
    }
}

/// Produce `" "` * (4 * depth).
fn indent(depth: usize) -> String {
    " ".repeat(4 * depth)
}

/// Same as `indent`, returned as an owned `String` for inline `writeln!`.
fn indent_for(depth: usize) -> String {
    indent(depth)
}

