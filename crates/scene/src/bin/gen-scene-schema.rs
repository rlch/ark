//! `gen-scene-schema` — emit a KDL schema for the scene AST by walking
//! facet SHAPE reflection on every `#[derive(Facet)]` type in the scene
//! grammar.
//!
//! T-140 (cavekit-scene R13 / Tier 15).
//!
//! Run:
//! ```sh
//! cargo run -p ark-scene --bin gen-scene-schema
//! ```
//!
//! Output is written to stdout. Pipe to a file or inspect interactively:
//! ```sh
//! cargo run -p ark-scene --bin gen-scene-schema > scene-schema.kdl
//! ```
//!
//! # Design
//!
//! The binary walks the scene AST's facet SHAPE graph starting from
//! `SceneDoc` (the top-level wrapper) and emits a KDL schema document
//! describing every node type, its fields (with types, required/optional
//! status, and doc-comments), and enum variants. The schema mirrors the
//! approach used in `gen-extension-spec` (crates/ark-ext-proto) but
//! targets the scene grammar instead of the extension protocol.

use std::collections::BTreeMap;
use std::fmt::Write as _;

use facet::{Def, Facet, Shape, Type, UserType};

use ark_scene::ast::layout::{ColNode, Handle, PaneNode, RowNode, TabNode};
use ark_scene::ast::ops::{
    CloseOp, EmitOp, ExecOp, FocusOp, MoveOp, NewTabOp, OpNode, PinOp, PipeOp, ReloadSceneOp,
    RenameOp, ResizeOp, SetStatusOp, SpawnOp, UnpinOp, UseModeOp,
};
use ark_scene::ast::selector::EventSelector;
use ark_scene::ast::{
    BindNode, ClearBindNode, ClearReactionsNode, DisableExtensionNode, IncludeNode, LayoutNode,
    ModeNode, OnNode, SceneBodyNode, SceneDoc, SceneNode, UseNode,
};

/// Type index: every user struct / enum reachable from the scene AST.
type TypeIndex = BTreeMap<&'static str, &'static Shape>;

fn main() {
    let doc = generate_scene_schema_kdl();
    print!("{doc}");
}

/// Produce the full scene schema KDL document.
fn generate_scene_schema_kdl() -> String {
    let mut types: TypeIndex = BTreeMap::new();

    // Seed the type index with every root AST type.
    let root_shapes: &[&'static Shape] = &[
        SceneDoc::SHAPE,
        SceneNode::SHAPE,
        SceneBodyNode::SHAPE,
        UseNode::SHAPE,
        IncludeNode::SHAPE,
        LayoutNode::SHAPE,
        ModeNode::SHAPE,
        OnNode::SHAPE,
        BindNode::SHAPE,
        ClearReactionsNode::SHAPE,
        ClearBindNode::SHAPE,
        DisableExtensionNode::SHAPE,
        // Layout types
        TabNode::SHAPE,
        RowNode::SHAPE,
        ColNode::SHAPE,
        PaneNode::SHAPE,
        Handle::SHAPE,
        // Op types
        OpNode::SHAPE,
        FocusOp::SHAPE,
        CloseOp::SHAPE,
        RenameOp::SHAPE,
        ResizeOp::SHAPE,
        MoveOp::SHAPE,
        PinOp::SHAPE,
        UnpinOp::SHAPE,
        SpawnOp::SHAPE,
        NewTabOp::SHAPE,
        UseModeOp::SHAPE,
        PipeOp::SHAPE,
        EmitOp::SHAPE,
        SetStatusOp::SHAPE,
        ExecOp::SHAPE,
        ReloadSceneOp::SHAPE,
        // Selector
        EventSelector::SHAPE,
    ];

    for shape in root_shapes {
        collect_user_types(shape, &mut types);
    }

    let mut out = String::new();
    writeln!(
        &mut out,
        "// Generated by `cargo run -p ark-scene --bin gen-scene-schema`."
    )
    .unwrap();
    writeln!(
        &mut out,
        "// DO NOT EDIT BY HAND. Source of truth: crates/scene/src/ast/"
    )
    .unwrap();
    writeln!(&mut out, "// (walked via facet SHAPE reflection).").unwrap();
    writeln!(&mut out).unwrap();
    writeln!(&mut out, "scene-schema {{").unwrap();
    writeln!(&mut out, "    info {{").unwrap();
    writeln!(&mut out, "        title \"ark scene grammar (v1)\"").unwrap();
    writeln!(
        &mut out,
        "        description \"KDL schema for the ark v3 scene AST. Each type maps to a KDL node shape in the scene file.\""
    )
    .unwrap();
    writeln!(&mut out, "        version \"1.0.0\"").unwrap();
    writeln!(&mut out, "    }}").unwrap();
    writeln!(&mut out).unwrap();

    // Emit definitions for every collected type.
    writeln!(&mut out, "    definitions {{").unwrap();
    for (name, shape) in &types {
        emit_definition(&mut out, name, shape, 2);
    }
    writeln!(&mut out, "    }}").unwrap();
    writeln!(&mut out, "}}").unwrap();
    out
}

/// Emit a type definition block.
fn emit_definition(out: &mut String, id: &str, shape: &'static Shape, depth: usize) {
    let ind = indent(depth);
    writeln!(out, "{ind}type \"{id}\" {{").unwrap();
    write_doc(out, shape.doc, depth + 1);

    match &shape.ty {
        Type::User(UserType::Struct(st)) => {
            writeln!(out, "{}kind \"struct\"", indent(depth + 1)).unwrap();
            if !st.fields.is_empty() {
                writeln!(out, "{}fields {{", indent(depth + 1)).unwrap();
                for field in st.fields {
                    emit_field(out, field, depth + 2);
                }
                writeln!(out, "{}}}", indent(depth + 1)).unwrap();
            }
        }
        Type::User(UserType::Enum(en)) => {
            writeln!(out, "{}kind \"enum\"", indent(depth + 1)).unwrap();
            writeln!(out, "{}variants {{", indent(depth + 1)).unwrap();
            for variant in en.variants {
                write!(out, "{}variant \"{}\"", indent(depth + 2), variant.name).unwrap();
                if !variant.doc.is_empty() {
                    let doc_joined = variant
                        .doc
                        .iter()
                        .map(|l| l.trim())
                        .collect::<Vec<_>>()
                        .join(" ");
                    let escaped = doc_joined.replace('\\', "\\\\").replace('"', "\\\"");
                    write!(out, " description=\"{escaped}\"").unwrap();
                }
                writeln!(out).unwrap();
            }
            writeln!(out, "{}}}", indent(depth + 1)).unwrap();
        }
        _ => {
            writeln!(out, "{}kind \"opaque\"", indent(depth + 1)).unwrap();
        }
    }

    writeln!(out, "{ind}}}").unwrap();
}

/// Emit a single field entry.
fn emit_field(out: &mut String, field: &facet::Field, depth: usize) {
    let name = field.rename.unwrap_or(field.name);
    let ind = indent(depth);
    let (inner, optional, plural) = unwrap_container(field.shape());
    let type_name = scalar_type_name(inner);

    writeln!(out, "{ind}field \"{name}\" {{").unwrap();
    write_doc(out, field.doc, depth + 1);
    writeln!(out, "{}type \"{type_name}\"", indent(depth + 1)).unwrap();
    writeln!(
        out,
        "{}plural {}",
        indent(depth + 1),
        if plural { "#true" } else { "#false" }
    )
    .unwrap();
    writeln!(
        out,
        "{}required {}",
        indent(depth + 1),
        if optional { "#false" } else { "#true" }
    )
    .unwrap();
    writeln!(out, "{ind}}}").unwrap();
}

// ---------------------------------------------------------------------------
// SHAPE walking helpers
// ---------------------------------------------------------------------------

/// Walk a SHAPE graph, inserting every user struct / enum into `acc`.
fn collect_user_types(shape: &'static Shape, acc: &mut TypeIndex) {
    let user = match inner_user_shape(shape) {
        Some(u) => u,
        None => return,
    };
    if acc.contains_key(user.type_identifier) {
        return;
    }
    acc.insert(user.type_identifier, user);

    match &user.ty {
        Type::User(UserType::Struct(st)) => {
            for field in st.fields {
                collect_user_types(field.shape(), acc);
            }
        }
        Type::User(UserType::Enum(en)) => {
            for variant in en.variants {
                for field in variant.data.fields {
                    collect_user_types(field.shape(), acc);
                }
            }
        }
        _ => {}
    }
}

/// Peel Option/Vec wrappers to find the innermost user type.
fn inner_user_shape(shape: &'static Shape) -> Option<&'static Shape> {
    match &shape.ty {
        Type::User(UserType::Struct(_))
        | Type::User(UserType::Enum(_))
        | Type::User(UserType::Union(_)) => Some(shape),
        _ => match &shape.def {
            Def::Option(o) => inner_user_shape(o.t),
            Def::List(l) => inner_user_shape(l.t()),
            _ => None,
        },
    }
}

/// Walk `Option<...>` / `Vec<...>` wrappers. Returns
/// `(inner, is_optional, is_plural)`.
fn unwrap_container(shape: &'static Shape) -> (&'static Shape, bool, bool) {
    match &shape.def {
        Def::Option(o) => {
            let (inner, _, plural) = unwrap_container(o.t);
            (inner, true, plural)
        }
        Def::List(l) => {
            let (inner, optional, _) = unwrap_container(l.t());
            (inner, optional, true)
        }
        _ => (shape, false, false),
    }
}

/// Map a shape to a human-readable type tag.
fn scalar_type_name(shape: &'static Shape) -> &'static str {
    match shape.type_identifier {
        "String" | "str" => "string",
        "bool" => "bool",
        "u32" => "u32",
        "u64" => "u64",
        "i32" => "i32",
        "i64" => "i64",
        other => other,
    }
}

/// Write doc-comment as a KDL `description` string.
fn write_doc(out: &mut String, doc: &'static [&'static str], depth: usize) {
    if doc.is_empty() {
        return;
    }
    let joined = doc
        .iter()
        .map(|line| line.trim())
        .collect::<Vec<_>>()
        .join(" ");
    let escaped = joined.replace('\\', "\\\\").replace('"', "\\\"");
    writeln!(out, "{}description \"{}\"", indent(depth), escaped).unwrap();
}

fn indent(depth: usize) -> String {
    " ".repeat(4 * depth)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn schema_contains_core_types() {
        let schema = generate_scene_schema_kdl();
        for expected in &[
            "SceneDoc",
            "SceneNode",
            "SceneBodyNode",
            "OpNode",
            "TabNode",
            "PaneNode",
        ] {
            let needle = format!("type \"{expected}\"");
            assert!(
                schema.contains(&needle),
                "schema missing definition for {expected}"
            );
        }
    }

    #[test]
    fn schema_non_empty() {
        let schema = generate_scene_schema_kdl();
        assert!(
            schema.len() > 500,
            "schema suspiciously short: {} bytes",
            schema.len()
        );
    }
}
